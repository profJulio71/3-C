"""
Crie duas tuplas, tupla1 e tupla2, cada uma com elementos diferentes. Em seguida, crie uma
nova tupla chamada tupla_concat que contenha todos os elementos de ambas as tuplas.
"""

# Criando as duas tuplas
tupla1 = (1, 2, 3, 4, 5)
tupla2 = ('a', 'b', 'c', 'd', 'e')

# Concatenando as tuplas
tupla_concat = tupla1 + tupla2

# Imprimindo a nova tupla
print(tupla_concat)