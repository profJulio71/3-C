lista = [0, 2, 4, 8]
print(f"Os números são: {lista}")

# Acrescente o valor 10 ao final da lista.
lista.append(10)
print(f"\nAcrescentado o valor 10 ao final da lista\n {lista}")

# Acrescentar o valor 6 na posição 3.
lista.insert(3, 6)
print(f"\nAcrescentado o valor 6 na posição 3\n {lista}")