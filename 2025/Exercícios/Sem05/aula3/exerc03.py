lista = [8, 1, 0, 5, 13, 1, 3, 2]
print(f"Os números da lista são: {lista}")
# Ordene a lista.

print(f"\nOs números da lista em ordem crescente: {sorted(lista)}")

# existe outra forma de fazer a ordenação?
lista.sort()
print(f"\nOutra forma de ordenar a lista: {lista}")

# Coloque em valor reverso a lista Fibonacci.
lista.reverse()
print(f"\nOs números da lista em ordem reversa: {lista}")