# Remova o elemento de índice 2 da lista e mostre o
# elemento removido.
lista = ["Alice", "Bob", "Charlie", "David"]
rem = lista[2]  # Atribui a variável rem o valor de índice 2
lista.remove(rem) # Remove da lista o valor que está na variável rem.
print(f"\nO valor removido da lista da lista é:\n{rem}")
