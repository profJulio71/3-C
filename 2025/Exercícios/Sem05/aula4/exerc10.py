# Dada a lista:
lista_numerica = [15, 8, 22, 3, 10]

# a) Mostre o tamanho da lista.
print(f"\nO tamanho da lista é: {len(lista_numerica)}")

# b) Mostre o maior valor da lista.
print(f"\nO maior valor da lista é: {max(lista_numerica)}")

# c) Mostre o menor valor da lista.
print(f"\nO menor valor da lista é: {min(lista_numerica)}")

# d) Mostre a soma de todos os elementos da lista.
print(f"\nA soma de todos os valores da lista é: {sum(lista_numerica)}")